        title.className = 'card-detail__title';
        title.textContent = card.title;
        
        const meta = document.createElement('div');
        meta.className = 'card-detail__meta';
        
        const created = document.createElement('span');
        created.textContent = `‰ΩúÊàê: ${Utils.formatDateTime(card.created_at)}`;
        
        const reviews = document.createElement('span');
        reviews.textContent = `Âæ©ÁøíÂõûÊï∞: ${card.review_count || 0}Âõû`;
        
        meta.appendChild(created);
        meta.appendChild(reviews);
        
        header.appendChild(title);
        header.appendChild(meta);
        
        // „Ç≥„É≥„ÉÜ„É≥„ÉÑ
