        frontTitle.className = 'card-detail__side-title';
        frontTitle.textContent = '„Ç™„É¢„ÉÜ';
        
        frontSide.appendChild(frontTitle);
        frontSide.appendChild(this.renderCardContent(card, 'front'));
        
        // „Ç¶„É©
